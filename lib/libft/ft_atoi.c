#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sum;
	int	sign;
	int	i;

	sum = 0;
	sign = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		sum = sum * 10 + (str[i++] - '0');
	return (sign * sum);
}
